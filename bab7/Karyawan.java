package bab7;
public class Karyawan {
    private String nama ; 
    private float gaji ;
    private int usia ;
    
    public String getNama(){
        return nama ;
    }
    
    public void setNama(String a){
        this.nama = a ;
    }
    
    public float getGaji(){
        return gaji ;
    }
    
    public void setGaji(float b){
        this.gaji = b ;
    }
    
    public int getUsia(){
        return usia ;
    }
    
    public void setUsia(int c){
        this.usia = c ;
    }
}
